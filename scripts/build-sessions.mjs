import { readFileSync, readdirSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";

const PROMPTS_DIR = "/Users/eudaimonia/Desktop/voiceAI/agent/prompts";
const OUT_FILE = "/Users/eudaimonia/Desktop/voiceAI/app/web/lib/sessions-data.ts";

const files = readdirSync(PROMPTS_DIR)
  .filter((f) => /^Session_\d+_.+\.txt$/i.test(f))
  .sort();

const sessions = files.map((file) => {
  const match = file.match(/^Session_(\d+)_(.+)\.txt$/i);
  const no = Number(match[1]);
  const slug = match[2];
  const content = readFileSync(join(PROMPTS_DIR, file), "utf8");
  // Title from "THEME: ..." line, fallback to filename slug.
  const themeMatch = content.match(/^\s*THEME:\s*(.+)$/m);
  const title = themeMatch ? themeMatch[1].trim() : slug.replace(/_/g, " ");
  // Week from header line "Week N"
  const weekMatch = content.match(/Week\s+(\d+)/i);
  const week = weekMatch ? Number(weekMatch[1]) : Math.ceil(no / 4);
  return { no, slug, title, week, prompt: content };
});

if (sessions.length !== 24) {
  console.warn(`expected 24 prompts, found ${sessions.length}`);
}

const ts = `// AUTO-GENERATED by scripts/build-sessions.mjs — do not edit by hand.
// Regenerate after touching agent/prompts/.

export type Session = {
  no: number;
  slug: string;
  title: string;
  week: number;
  prompt: string;
};

export const SESSIONS: Session[] = ${JSON.stringify(sessions, null, 2)};
`;

mkdirSync(dirname(OUT_FILE), { recursive: true });
writeFileSync(OUT_FILE, ts);
console.log(`Wrote ${sessions.length} sessions → ${OUT_FILE} (${(ts.length / 1024).toFixed(0)} KB)`);
